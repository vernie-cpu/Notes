"""
插入排序：
    - 将序列分为有序和无序两部分，外层循环一次就是把无序部分第一个元素插入到有序部分合适的位置去
    - 默认有序部分就是序列的第一个元素，余下的是无序部分
"""

a = [6, 4, 2, 1, 7, 5, 11, 22, 17, 23, 14, 3, 9]

for i in range(1, len(a)):
    for k in range(i, 0, -1):
        if a[k] >= a[k - 1]:
            break
        a[k], a[k - 1] = a[k - 1], a[k]

print(a)

'''
时间复杂度分析:
    - 比较次数: (n-1) + (n-2) + (n-3) .... + 1 = (n-1)*((n-1) + 1)/2 = (n^2 - n)/2
    - 交换次数: (n-1) + (n-2) + (n-3) .... + 1 = (n-1)*((n-1) + 1)/2 = (n^2 - n)/2
    _ 时间复杂度: O(n^2)
'''